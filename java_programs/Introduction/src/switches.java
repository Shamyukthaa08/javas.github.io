public class switches {
    public static void main(String[] args){
        int a=10;
        switch(a){
        case 10:
        case 20:
        case 30:
        case 40:
        case 50:
        case 60:
        case 70:
        case 80:
        case 90:
            System.out.println("It is divisible by 10");
            break;
        default:
        System.out.println("It is not divisible by 10");
    }
        
    }
    
}
